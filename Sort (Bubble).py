def bubbleSort( alist ):

    for passnum in list( range(len(alist) - 1,0,-1) ):
        for i in range(passnum):
            print('COMPARE[',i, i+1,'] = ', alist)
            
            if alist[i] > alist[i+1]:
                temp = alist[i+1]
                alist[i+1] = alist[i]
                alist[i] = temp
                print('SWAP[',i, i+1,'] = ', alist)
                
        print('-'*50)
    
    
    
bubbleSort( [874,572,990,891,28,56] )


